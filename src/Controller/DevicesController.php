        $result = Hash::combine($devices, '{n}.id', '{n}.adgroup_id');
        $Adgroups = array();
        if (!empty($result)) {
            $Adgroups = $this->Adgroups->find()->where(['id IN' => array_unique($result)])->combine('id', 'name')->toArray();
        }
        $this->set(compact('Adgroups','devices'));
    }


